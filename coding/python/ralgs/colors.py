class Colors:
    def __init__(self):
        self.r = "\033[31m" # Red
        self.g = "\033[32m" # Green
        self.o = "\033[38;5;214m" # Orange
        self.y = "\033[33m" # Yellow
        self.b = "\033[34m" # Blue
        self.w = "\033[97m" # White

        self.reset = "\033[0m" # Reset colors; DO NOT CHANGE!
